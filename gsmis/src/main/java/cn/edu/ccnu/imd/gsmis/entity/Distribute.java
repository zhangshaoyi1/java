package cn.edu.ccnu.imd.gsmis.entity;

import com.thinkgem.jeesite.common.persistence.DataEntity;

/**
 * 论文分配管理Entity
 * @author zjy
 * @version 2018-08-14
 */
public class Distribute extends DataEntity<Distribute> {
	
	private static final long serialVersionUID = 1L;
	private String major;			// 专业
	private String masterName;		// 硕士名
	private String topic;			// 论文主题
	private String studentID;			// 学号
	private String keywords;			// 关键字
	private String status;			// 分配状态
	private String thesisid;			// 论文id
	
	public Distribute() {
		super();
	}

	public Distribute(String id){
		super(id);
	}

	public String getMajor() {
		return major;
	}

	public void setMajor(String major) {
		this.major = major;
	}

	public String getMasterName() {
		return masterName;
	}

	public void setMasterName(String masterName) {
		this.masterName = masterName;
	}

	public String getTopic() {
		return topic;
	}

	public void setTopic(String topic) {
		this.topic = topic;
	}

	public String getStudentID() {
		return studentID;
	}

	public void setStudentID(String studentID) {
		this.studentID = studentID;
	}

	public String getKeywords() {
		return keywords;
	}

	public void setKeywords(String keywords) {
		this.keywords = keywords;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getThesisid() {
		return thesisid;
	}

	public void setThesisid(String thesisid) {
		this.thesisid = thesisid;
	}

	
	
	
	
	
}